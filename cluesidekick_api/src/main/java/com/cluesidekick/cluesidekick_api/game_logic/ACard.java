package com.cluesidekick.cluesidekick_api.game_logic;

/**
 * This class represents a card in the ClueGame.
 */
public class ACard {

    private String name;

    /**
     * Constructor to create a card with a given name.
     *
     * @param name The name of the card.
     */
    public ACard(String name) {
        this.name = name;
    }

}
